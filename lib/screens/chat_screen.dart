  void initState() {
    super.initState();

    getCurrentUser();
  }

  void getCurrentUser() {
    final user = _auth.currentUser;

    if (user != null) {
      loggedInUser = user;
      //print(loggedInUser.email);
    }
  }

  // void getMessages() async {
